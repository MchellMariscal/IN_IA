def logica_modal(posible, necesario):
    if necesario:  # Si es necesario
        return "Es necesario que sea verdadero."  # Devolvemos que es necesario
    elif posible:  # Si es posible
        return "Es posible que sea verdadero."  # Devolvemos que es posible
    return "No es ni necesario ni posible."  # Devolvemos que no es ni necesario ni posible

# Ejemplo de aplicación real: En la filosofía y la inteligencia artificial, la lógica modal puede ser utilizada para razonar sobre la necesidad y posibilidad de proposiciones, considerando diferentes mundos posibles.
