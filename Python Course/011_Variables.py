# Sección 4 – Variables

# Asignando valores a las variables
nombre = "Michell"  # Mi nombre
edad = 20  # Mi edad
altura = 1.62  # Mi altura
es_estudiante = True  # Soy estudiante

# Imprimiendo los valores de las variables
print("Nombre:", nombre)
print("Edad:", edad)
print("Altura:", altura)
print("Es estudiante:", es_estudiante)

# Cambiando algunos valores de las variables
edad = 21  # Cambio mi edad
es_estudiante = False  # Ya no soy estudiante

# Imprimiendo los valores modificados
print("\nValores modificados:")
print("Edad modificada:", edad)
print("Es estudiante (modificado):", es_estudiante)

# Realizando algunas operaciones con las variables
suma_edad_altura = edad + altura  # Sumando la edad y altura
producto_edad_altura = edad * altura  # Multiplicando la edad por la altura

# Imprimiendo los resultados de las operaciones
print("\nOperaciones con variables:")
print("Suma de edad y altura:", suma_edad_altura)
print("Producto de edad y altura:", producto_edad_altura)

# Concatenando variables para hacer una frase
frase = "Mi nombre es " + nombre + " y tengo " + str(edad) + " años."
print("\nFrase concatenada:", frase)

# Mostrando el tipo de las variables
print("\nTipos de las variables:")
print("Tipo de la variable 'nombre':", type(nombre))
print("Tipo de la variable 'edad':", type(edad))
print("Tipo de la variable 'altura':", type(altura))
print("Tipo de la variable 'es_estudiante':", type(es_estudiante))
