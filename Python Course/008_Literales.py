# Parte 1: Literales enteros
# Aquí estoy usando números enteros
entero_positivo = 150
entero_negativo = -42
entero_grande = 1000000

# Imprimir literales enteros
print("Literal entero positivo:", entero_positivo)
print("Literal entero negativo:", entero_negativo)
print("Literal entero grande:", entero_grande)

# Parte 2: Literales flotantes
# En esta parte pruebo con números flotantes
flotante_positivo = 3.14159
flotante_negativo = -0.007
flotante_cientifico = 1e3  # 1 * 10^3 = 1000.0

# Imprimir literales flotantes
print("Literal flotante positivo:", flotante_positivo)
print("Literal flotante negativo:", flotante_negativo)
print("Literal flotante en notación científica:", flotante_cientifico)

# Parte 3: Literales de cadenas
# Aquí estoy trabajando con cadenas de texto
cadena_simple = "Hola, ¿cómo estás?"
cadena_con_comillas = "Él dijo: 'El mapache es hermoso'."
cadena_espacios = "  Espacios antes y después  "

# Imprimir literales de cadenas
print("Cadena simple:", cadena_simple)
print("Cadena con comillas dentro:", cadena_con_comillas)
print("Cadena con espacios:", cadena_espacios)

# Parte 4: Operaciones con literales
# Ahora hago algunas operaciones con los literales numéricos
# Operación con enteros
suma_enteros = entero_positivo + entero_negativo
multiplicacion_enteros = entero_grande * 2

# Operación con flotantes
suma_flotantes = flotante_positivo + flotante_negativo
producto_flotantes = flotante_cientifico * 0.5

# Imprimir resultados de operaciones
print("Suma de enteros:", suma_enteros)
print("Multiplicación de enteros:", multiplicacion_enteros)
print("Suma de flotantes:", suma_flotantes)
print("Producto de flotantes:", producto_flotantes)

# Parte 5: Conversión de literales
# Aquí pruebo la conversión entre tipos de datos
# Convertir entero a flotante
entero_a_flotante = float(entero_positivo)

# Convertir flotante a entero
flotante_a_entero = int(flotante_positivo)

# Imprimir conversiones
print("Entero convertido a flotante:", entero_a_flotante)
print("Flotante convertido a entero:", flotante_a_entero)

# Parte 6: Ejemplo de uso de literales en una frase
# Ahora meto algunos literales en una frase
nombre = "Michell"
edad = 20
print(f"Mi nombre es {nombre} y tengo {edad} años.")
